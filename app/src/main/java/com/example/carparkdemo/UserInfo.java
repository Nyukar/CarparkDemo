package com.example.carparkdemo;

public class UserInfo {

    private String name;
    private String phoneNum;
    private String cColour;
    private String cMake;
    private String cModel;
    private String plate;
    private String email;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhoneNum() {
        return phoneNum;
    }

    public void setPhoneNum(String phoneNum) {
        this.phoneNum = phoneNum;
    }

    public String getcColour() {
        return cColour;
    }

    public void setcColour(String cColour) {
        this.cColour = cColour;
    }

    public String getcMake() {
        return cMake;
    }

    public void setcMake(String cMake) {
        this.cMake = cMake;
    }

    public String getcModel() {
        return cModel;
    }

    public void setcModel(String cModel) {
        this.cModel = cModel;
    }

    public String getPlate() {
        return plate;
    }

    public void setPlate(String plate) {
        this.plate = plate;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
